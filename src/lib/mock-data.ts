// Function to generate more meetings for infinite scroll
export function generateMoreMeetings(startIndex: number, count: number): Meeting[] {
  const statuses: Meeting["status"][] = ["upcoming", "past"];
  const titles = [
    "Team Standup",
    "Product Review",
    "Strategy Session",
    "Client Call",
    "Design Critique",
    "Tech Planning",
    "Budget Review",
    "Quarterly Review",
  ];

  return Array.from({ length: count }, (_, i) => {
    const index = startIndex + i;
    const isPast = index % 3 === 0;
    return {
      id: `m-gen-${index}`,
      title: titles[index % titles.length],
      date: isPast ? `Jan ${Math.max(1, 15 - (index % 15))}` : `Feb ${(index % 28) + 1}`,
      time: `${9 + (index % 8)}:00 - ${10 + (index % 8)}:00`,
      participantCount: 5 + (index % 10),
      status: isPast ? "past" : "upcoming",
      aiEnabled: index % 2 === 0,
      ...(isPast && {
        previousSummary: `Summary for meeting ${index}. Key decisions were made and action items assigned.`,
      }),
    };
  });
}
